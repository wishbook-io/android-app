package com.wishbook.catalog.commonmodels;

public class RequestBureau {

    public String bureau_score;

    public String bureau_type;

    public String bureau_status;

    public String bureau_xml;

    public String bureau_order_id;

    public String bureau_report_id;

    public String company;

    public String getBureau_score() {
        return bureau_score;
    }

    public void setBureau_score(String bureau_score) {
        this.bureau_score = bureau_score;
    }

    public String getBureau_type() {
        return bureau_type;
    }

    public void setBureau_type(String bureau_type) {
        this.bureau_type = bureau_type;
    }

    public String getBureau_status() {
        return bureau_status;
    }

    public void setBureau_status(String bureau_status) {
        this.bureau_status = bureau_status;
    }

    public String getBureau_xml() {
        return bureau_xml;
    }

    public void setBureau_xml(String bureau_xml) {
        this.bureau_xml = bureau_xml;
    }

    public String getBureau_order_id() {
        return bureau_order_id;
    }

    public void setBureau_order_id(String bureau_order_id) {
        this.bureau_order_id = bureau_order_id;
    }

    public String getBureau_report_id() {
        return bureau_report_id;
    }

    public void setBureau_report_id(String bureau_report_id) {
        this.bureau_report_id = bureau_report_id;
    }

    public String getCompany() {
        return company;
    }

    public void setCompany(String company) {
        this.company = company;
    }
}
